const generateRandomColor = () => {
  let color = "#";
  const letters = "1234567890ABCDEF";
  for (let i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * letters.length)];
  }
  return (document.body.style.background = color);
};

